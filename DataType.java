

class DataType{

     public static void main(String[] args) {
    
        //Int datatype
    int p1=Integer.MAX_VALUE;
    int p2=Integer.MIN_VALUE;

    System.out.println("int max value"+p1+" "+"int min value"+p2);

    //Byte data type
    byte b1=Byte.MAX_VALUE;
    byte b2=Byte.MIN_VALUE;

    System.out.println("byte max and min values:"+b1+" "+b2);

//short data type
short s1=Short.MAX_VALUE;
short s2=Short.MIN_VALUE;
System.out.println("short max and min values:"+s1+" "+s2);


//long data type
long l1=Long.MAX_VALUE;
long l2=Long.MIN_VALUE;
System.out.println("long max and min values:"+l1+" "+l2);



//Float data type

float f1=Float.MAX_VALUE;
float f2=Float.MIN_VALUE;
System.out.println("float max and min value:"+f1+" "+f2);

//Double data type

double d1=Double.MAX_VALUE;
double d2=Double.MIN_VALUE;
System.out.println("double max and min value:"+d1+" "+d2);

//Char data type

char c1='1';
System.out.println(c1);



char unicode='\u013F';
System.out.println(unicode);

// String data type

String str1="my name ana";
String str2="20";
int i1=2345;

System.out.println(str1+str2+i1);










     }
}